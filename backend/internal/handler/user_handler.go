package handler

import (
	"net/http"

	"user-management-system/internal/logic"
	"user-management-system/internal/svc"
	"user-management-system/types"

	"github.com/zeromicro/go-zero/rest/httpx"
)

// RegisterHandler 用户注册
func RegisterHandler(serverCtx *svc.ServiceContext) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		var req types.RegisterRequest
		if err := httpx.Parse(r, &req); err != nil {
			httpx.ErrorCtx(r.Context(), w, err)
			return
		}

		l := logic.NewRegisterLogic(r.Context(), serverCtx)
		resp, err := l.Register(&req)
		if err != nil {
			httpx.ErrorCtx(r.Context(), w, err)
		} else {
			httpx.OkJsonCtx(r.Context(), w, resp)
		}
	}
}

